package mar22.week1.day2;

public class Age {

	public static void main(String[] args) {
			int age = 35;
			
			if (age<18)
				System.out.println("This is the age of a child");
			
			else if (age>=18 && age<60)
			System.out.println("This is the age of an adult");
			
			else
				System.out.println("This is the age of a senior");
				
	}

}
